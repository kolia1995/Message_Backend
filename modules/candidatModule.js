const mongoose = require('mongoose');

const candidatSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    },
    code: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
        expires: 300,
    }
});

const candidatModule = mongoose.model('candidat', candidatSchema);
module.exports = candidatModule;